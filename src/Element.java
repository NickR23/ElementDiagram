
public class Element {
	private int electrons;
	private int protons;
	private String name;
	
	public void setElectrons(int e) {
		electrons = e;
	}
	public void setProtons(int p) {
		protons = p;
	}
	public void setName(String n) {
		name = n;
	}
	
	public int getElectrons() {
		return electrons;
	}
	public int getProtons() {
		return protons;
	}
	public String getName() {
		return name;
	}
}
